# Armazenando valores em variáveis
var1 = 5
var2 = 2

# Imprimindo o tipo de dados das duas variáveis
print(type(var1))  # Imprime o tipo de var1 (esperado: <class 'int'>)
print(type(var2))  # Imprime o tipo de var2 (esperado: <class 'int'>)

# Dividindo var1 por var2 e armazenando o resultado em var3
var3 = var1 / var2

# Imprimindo o valor e o tipo de var3
print(var3)         # Imprime o resultado da divisão (esperado: 2.5)
print(type(var3))   # Imprime o tipo de var3 (esperado: <class 'float'>)
