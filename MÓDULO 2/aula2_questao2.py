# Armazenando valores nas variáveis
texto = "o resultado é"
valor1 = 10
valor2 = 3.5

# Somando os valores da segunda e terceira variável e armazenando o resultado
soma = valor1 + valor2

# Imprimindo todas as variáveis na ordem de criação e seus tipos
print(texto)          # Imprime o texto
print(type(texto))    # Imprime o tipo de texto (esperado: <class 'str'>)

print(valor1)         # Imprime o valor da segunda variável
print(type(valor1))   # Imprime o tipo de valor1 (esperado: <class 'int'>)

print(valor2)         # Imprime o valor da terceira variável
print(type(valor2))   # Imprime o tipo de valor2 (esperado: <class 'float'>)

print(soma)           # Imprime o resultado da soma
print(type(soma))     # Imprime o tipo de soma (esperado: <class 'float'>)
